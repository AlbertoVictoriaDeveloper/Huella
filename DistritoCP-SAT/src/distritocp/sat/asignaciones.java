    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
     try{
     asignacion usuarios = (asignacion)cboUsuario.getSelectedItem();
     Integer idUsuario = usuarios.getIdUsuario();
     accesosActivos accesos = (accesosActivos)cboAccesos.getSelectedItem(); 
     Integer idAcceso = accesos.getIDAcceso(); 
     Integer idAsignacion = consultas.setIDAsignacion(idAcceso); 
     if(consultas.actualizarAsignacion(idAsignacion, idUsuario, idAcceso)){
       System.out.println("actulizado"); 
       mostrarAsignaciones("");
        cboUsuario.setSelectedIndex(0);
        cboAccesos.setSelectedIndex(0);
     }else{
      JOptionPane.showMessageDialog(null, "No se logro asignar el acceso", "Ocurrio Un Error", JOptionPane.WARNING_MESSAGE);    
     
     }
     
     }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Favor de llenar los datos requeridos", "Ocurrio Un Error", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_btnAsignarActionPerformed

    private void tbAsignacionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbAsignacionesMouseClicked
        // TODO add your handling code here:รง
        int colum = tbAsignaciones.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / tbAsignaciones.getRowHeight(); 
        String  idAsignacion = String.valueOf(tbAsignaciones.getValueAt(row, 0));
        Integer  IDAsignacion = Integer.parseInt(idAsignacion);
       if(row < tbAsignaciones.getRowCount() && row >= 0  && colum < tbAsignaciones.getColumnCount() && colum >= 0 ) {
           Object value = tbAsignaciones.getValueAt(row, colum);
         if(value instanceof JButton){
         ((JButton) value).doClick();
          JButton boton = (JButton) value;
             
       if(boton.getName().equals("eliminarAsignacion")){
        if(consultas.asignacionesEliminadas(IDAsignacion, idUsuario)){
            System.out.println("eliminado");
            mostrarAsignaciones("");
        
        }
       
       }
       }  
       }
    }//GEN-LAST:event_tbAsignacionesMouseClicked


