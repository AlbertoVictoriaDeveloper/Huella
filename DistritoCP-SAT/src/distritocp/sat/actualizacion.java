    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            String nombre = txtNombre.getText();
            sitios paginas = (sitios) cboAccesos.getSelectedItem();
            Integer paginasID = paginas.getID();
            String usuario = txtUsuario.getText();
            String contrasena = new String(txtContrasena.getPassword());
           String accesoFiel= txtAccesoFiel.getText();
           JTextField txtAccesoFielCaja = txtAccesoFiel;
           if (distritoController.validacionCampos(nombre, usuario, contrasena,accesoFiel,txtAccesoFielCaja)) {
          JOptionPane.showMessageDialog(null, "Favor de llenar los datos requeridos", "Ocurrio Un Error", JOptionPane.WARNING_MESSAGE);
          } else {
                Integer IDAcceso = getidAcceso();
                Integer IDAsignacion = getAsignacion();
                Integer IDUsuario = getidUsuario();
              if (consultas.actualizarAcceso(IDAcceso, nombre, usuario, contrasena)) {
                    if (consultas.actualizarAsignaciones(IDAsignacion, IDUsuario, paginasID)) {
                      JOptionPane.showMessageDialog(null, "Se realizo la actualizacion", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                      System.out.println("Actualizado");
                      this.setVisible(false);
                      controlAcceso  principal = new controlAcceso();
                      principal.setVisible(true);
                 } else {
                       JOptionPane.showMessageDialog(null, "No se realizo la actualizacion", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "No se realizo la actualizacion", "Error", JOptionPane.ERROR_MESSAGE);
              }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Favor de llenar los datos requeridos", "Ocurrio Un Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

