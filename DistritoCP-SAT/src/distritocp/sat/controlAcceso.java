    private void btnGuardareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardareActionPerformed
        try {
            sitios paginas = (sitios) cboAcceso.getSelectedItem();
            Integer IdPagina = paginas.getID();
            String nombreAcceso = txtAcceso.getText();
            String Usuario = txtUsuario.getText();
            String contrasena = new String(txtContrasena.getPassword());
            String accesoFiel= txtAccesoFiel.getText();
            JTextField txtAccesoFielCaja = txtAccesoFiel;
            if (distritoController.validacionCampos(nombreAcceso, Usuario, contrasena,accesoFiel,txtAccesoFielCaja )) {
                JOptionPane.showMessageDialog(null, "Favor de llenar los datos requeridos", "Ocurrio Un Error", JOptionPane.WARNING_MESSAGE);
            } else {
                if (consultas.GuardarAccesos(nombreAcceso, Usuario, contrasena,accesoFiel)) {
                    Integer IDAcceso = consultas.setAccesos();
                    if (consultas.GuardarAsignaciones(idUsuario, IdPagina, IDAcceso)) {
                        JOptionPane.showMessageDialog(null, "Los datos fueron almacenados", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                        txtAcceso.setText("");
                        txtUsuario.setText("");
                        txtContrasena.setText("");
                        cboAcceso.setSelectedIndex(0);
                        mostrarDatos("");

                    } else {
                        JOptionPane.showMessageDialog(null, "El sitio no fue almacenado", "Ocurrio Un Error", JOptionPane.WARNING_MESSAGE);

                    }

                } else {
                    JOptionPane.showMessageDialog(null, "El sitio no fue almacenado", "Ocurrio Un Error", JOptionPane.WARNING_MESSAGE);

                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Favor de llenar los datos requeridos", "Ocurrio Un Error", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_btnGuardareActionPerformed

