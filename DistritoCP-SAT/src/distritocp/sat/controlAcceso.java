    private void tbDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDatosMouseClicked
        int colum = tbDatos.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / tbDatos.getRowHeight();
        String idAsignacion = String.valueOf(tbDatos.getValueAt(row, 0));
        Integer IDAsignacion = Integer.parseInt(idAsignacion);
        Integer accesosID = consultas.obtenerIDAcceso(IDAsignacion);
        
        String usuarioAcceso = String.valueOf(tbDatos.getValueAt(row, 3));
        String contrasenaUser = consultas.obtenerContrasena(IDAsignacion);
        String url = consultas.obtenerURL(IDAsignacion);
        String cuenta = consultas.obtenerCuenta(IDAsignacion);
        if (row < tbDatos.getRowCount() && row >= 0 && colum < tbDatos.getColumnCount() && colum >= 0) {
            Object value = tbDatos.getValueAt(row, colum);

            if (value instanceof JButton) {
                ((JButton) value).doClick();
                JButton boton = (JButton) value;

                if (boton.getName().equals("eliminar")) {

                    int s = JOptionPane.showConfirmDialog(null, "Eliminar Acceso", "Si/No", JOptionPane.YES_NO_OPTION);
                    if (s == 0) {

                        if (consultas.eliminarAsignacion(IDAsignacion)) {

                            if (consultas.eliminarAcceso(accesosID)) {
                                JOptionPane.showMessageDialog(null, "Acceso Eliminado Exitosamente ", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                                mostrarDatos("");

                            }
                        }

                    }
                }
                if (boton.getName().equals("modificar")) {
                    removeAll();
                    revalidate();
                    repaint();
                    if (actualizaciones != null) {
                        actualizaciones.setVisible(true);
                        actualizaciones.setidAsigacion(IDAsignacion);
                        actualizaciones.SetidUsuario(idUsuario);
                        actualizaciones.SetidAcceso(accesosID);

                    } else {
                        actualizaciones.dispose();
                        System.out.println("esta abierto");

                    }

                }

                if (boton.getName().equals("entrar")) {
                   if( verificacionAcceso !=null){
    
                   verificacionAcceso.setVisible(true);
                    removeAll();
                    revalidate();
                    repaint();
                    String acceso = "accesoAdmin";
                    datosVerificacion(usuarioAcceso, contrasenaUser, url, idUsuario,cuenta);
                    verificacionAcceso.setAcceso(acceso);
                   }else{
                      verificacionAcceso.dispose();
                   
                   } 
                    
                }
                
                if(boton.getName().equals("verContrasena")){
                  removeAll();
                  revalidate();
                  repaint();
                    verificacionAcceso.setVisible(true);
                  String acceso = "verContrasenas";
                  datosVerificacion(usuarioAcceso, contrasenaUser, url, idUsuario,cuenta);
                  verificacionAcceso.setAcceso(acceso);

                }
                
            }
        }

    }//GEN-LAST:event_tbDatosMouseClicked

